package org.openjfx;

public class ModelChildInfo {
	String dob, bloodgroup, hepb, rv, dtap, ipv, mmr, var, hepa, bcg, hpv;

	public ModelChildInfo(String dob, String bloodgroup, String hepb, String rv, String dtap, String ipv, String mmr,
			String var, String hepa, String bcg, String hpv) {
//		super();
		this.dob = dob;
		this.bloodgroup = bloodgroup;
		this.hepb = hepb;
		this.rv = rv;
		this.dtap = dtap;
		this.ipv = ipv;
		this.mmr = mmr;
		this.var = var;
		this.hepa = hepa;
		this.bcg = bcg;
		this.hpv = hpv;
	}

	public String getDob() {
		return dob;
	}

	public void setDob(String dob) {
		this.dob = dob;
	}

	public String getBloodgroup() {
		return bloodgroup;
	}

	public void setBloodgroup(String bloodgroup) {
		this.bloodgroup = bloodgroup;
	}

	public String getHepb() {
		return hepb;
	}

	public void setHepb(String hepb) {
		this.hepb = hepb;
	}

	public String getRv() {
		return rv;
	}

	public void setRv(String rv) {
		this.rv = rv;
	}

	public String getDtap() {
		return dtap;
	}

	public void setDtap(String dtap) {
		this.dtap = dtap;
	}

	public String getIpv() {
		return ipv;
	}

	public void setIpv(String ipv) {
		this.ipv = ipv;
	}

	public String getMmr() {
		return mmr;
	}

	public void setMmr(String mmr) {
		this.mmr = mmr;
	}

	public String getVar() {
		return var;
	}

	public void setVar(String var) {
		this.var = var;
	}

	public String getHepa() {
		return hepa;
	}

	public void setHepa(String hepa) {
		this.hepa = hepa;
	}

	public String getBcg() {
		return bcg;
	}

	public void setBcg(String bcg) {
		this.bcg = bcg;
	}

	public String getHpv() {
		return hpv;
	}

	public void setHpv(String hpv) {
		this.hpv = hpv;
	}
	
}
